p = float(input("–í–µ—Ä–æ—è—Ç–Ω–æ—Å—Ç –∑–∞ —É—Å–ø–µ—Ö (%): ")) / 100
k_max = int(input("–ú–∞–∫—Å–∏–º–∞–ª–µ–Ω –±—Ä–æ–π –æ–ø–∏—Ç–∏ –∑–∞ –∏–∑—á–∏—Å–ª–µ–Ω–∏–µ: "))

print("-" * 113)

cumulative = 0
for k in range(1, k_max + 1):
    probability = ((1 - p) ** (k - 1)) * p
    cumulative += probability
    print(f"–ü—ä—Ä–≤–∏ —É—Å–ø–µ—Ö –Ω–∞ {k} –æ–ø–∏—Ç: {probability * 100:.3f} % | "
          f"–ö—É–º—É–ª–∞—Ç–∏–≤–Ω–æ: {cumulative * 100:.3f} % | 1 –æ—Ç {1 / probability:_.2f}")

# –°—Ç–∞—Ç–∏—Å—Ç–∏—á–µ—Å–∫–∏ —Ö–∞—Ä–∞–∫—Ç–µ—Ä–∏—Å—Ç–∏–∫–∏
mean = 1 / p
variance = (1 - p) / (p ** 2)
std_dev = variance ** 0.5

print("-" * 113)
print(f"üìä –°–¢–ê–¢–ò–°–¢–ò–ß–ï–°–ö–ò –•–ê–†–ê–ö–¢–ï–†–ò–°–¢–ò–ö–ò:")
print(f"–°—Ä–µ–¥–Ω–∞ —Å—Ç–æ–π–Ω–æ—Å—Ç (Œº): {mean:.4f} –æ–ø–∏—Ç–∞")
print(f"–î–∏—Å–ø–µ—Ä—Å–∏—è (œÉ¬≤): {variance:.4f}")
print(f"–°—Ç–∞–Ω–¥–∞—Ä—Ç–Ω–æ –æ—Ç–∫–ª–æ–Ω–µ–Ω–∏–µ (œÉ): {std_dev:.4f}")
print("-" * 113)
